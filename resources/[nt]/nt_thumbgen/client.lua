-- ─── CONFIGURATION ───────────────────────────────────────────────────────────
-- PROP_HEADING: 0=North, 90=East, 180=South, 270=West (GTA is CLOCKWISE)
local PROP_HEADING  = 90
-- SPAWN_OFFSET: where the prop appears relative to your position when the session starts
local SPAWN_OFFSET  = vector3(0.0, 4.0, 0.0)
-- AUTO_DELAY: seconds to wait before auto-capture fires (when auto mode is ON)
local AUTO_DELAY    = 1.5
-- SKIP_EXISTING: when true the server skips props that already have an output file
local SKIP_EXISTING = false
-- ─────────────────────────────────────────────────────────────────────────────

local done      = false
local activeObj = nil
local inSession = false

AddEventHandler('onResourceStop', function(res)
    if res ~= GetCurrentResourceName() then return end
    if activeObj and DoesEntityExist(activeObj) then
        DeleteObject(activeObj)
        activeObj = nil
    end
end)

RegisterNetEvent('dnx_thumbgen:next')
AddEventHandler('dnx_thumbgen:next', function(success)
    done = true
end)

local function DrawLabel(x, y, text, scale, r, g, b)
    SetTextFont(4)
    SetTextProportional(false)
    SetTextScale(scale, scale)
    SetTextColour(r, g, b, 255)
    SetTextCentre(true)
    SetTextOutline()
    BeginTextCommandDisplayText("STRING")
    AddTextComponentSubstringPlayerName(text)
    EndTextCommandDisplayText(x, y)
end

RegisterCommand('dnx_thumbgen', function()
    if inSession then
        print('[DNX Thumbgen] Session already running.')
        return
    end
    inSession = true

    CreateThread(function()
        local ped      = PlayerPedId()
        local myPos    = GetEntityCoords(ped)
        local spawnPos = myPos + SPAWN_OFFSET

        SetEntityVisible(ped, false, false)

        local total      = #props
        local skipped    = 0
        local quit       = false
        local autoMode   = false  -- toggled with TAB
        local grabDist   = 4.0    -- carry distance when G is held (scroll to change)

        for i, modelName in ipairs(props) do
            if quit then break end

            local hash = GetHashKey(modelName)

            RequestModel(hash)
            local t = 0
            while not HasModelLoaded(hash) do
                Wait(100)
                t = t + 100
                if t >= 5000 then break end
            end

            if not HasModelLoaded(hash) then
                print(('[DNX Thumbgen] SKIP (model not loaded): %s'):format(modelName))
                skipped = skipped + 1
            else
                local obj = CreateObject(hash, spawnPos.x, spawnPos.y, spawnPos.z, false, false, false)
                activeObj = obj
                Wait(0)

                -- per-prop adjustable state (resets each prop)
                local pHeading = PROP_HEADING

                SetEntityHeading(obj, pHeading)
                Wait(0)
                FreezeEntityPosition(obj, true)
                Wait(300)

                local action      = nil
                local autoElapsed = 0.0

                while not action do
                    HideHudAndRadarThisFrame()

                    -- Always disable these to prevent weapon fire / reload
                    DisableControlAction(0, 24, true)  -- LMB
                    DisableControlAction(0, 45, true)  -- R
                    DisableControlAction(0, 37, true)  -- TAB

                    -- ── Grab gizmo: hold G (47) ───────────────────────────────
                    local gHeld = IsControlPressed(0, 47)
                    if gHeld then
                        -- Disable E and Q so they only rotate, not trigger game actions
                        DisableControlAction(0, 38, true)  -- E
                        DisableControlAction(0, 44, true)  -- Q

                        -- Scroll wheel: push/pull prop along the camera ray
                        if IsControlJustPressed(0, 241) then grabDist = math.min(grabDist + 0.5, 25.0) end
                        if IsControlJustPressed(0, 242) then grabDist = math.max(grabDist - 0.5,  0.5) end

                        -- Project camera ray → world position
                        local camPos = GetGameplayCamCoords()
                        local camRot = GetGameplayCamRot(2)
                        local radX   = math.rad(camRot.x)
                        local radZ   = math.rad(camRot.z)
                        local fwd    = vector3(
                            -math.sin(radZ) * math.abs(math.cos(radX)),
                             math.cos(radZ) * math.abs(math.cos(radX)),
                             math.sin(radX)
                        )
                        local np = camPos + fwd * grabDist
                        SetEntityCoords(obj, np.x, np.y, np.z, false, false, false, false)

                        -- Q = rotate CCW, E = rotate CW (while G held)
                        if IsDisabledControlPressed(0, 44) then pHeading = (pHeading - 2.0 + 360) % 360 end
                        if IsDisabledControlPressed(0, 38) then pHeading = (pHeading + 2.0)       % 360 end
                        SetEntityHeading(obj, pHeading)

                        autoElapsed = 0.0  -- reset auto timer while grabbing
                    end
                    -- ─────────────────────────────────────────────────────────

                    -- ── HUD ──────────────────────────────────────────────────
                    local autoLabel = autoMode
                        and ('AUTO  |  ' .. ('[%d / %d]'):format(i, total))
                        or  ('[%d / %d]'):format(i, total)
                    local autoColR = autoMode and 100 or 180
                    local autoColG = autoMode and 220 or 180
                    local autoColB = autoMode and 100 or 180

                    DrawLabel(0.5, 0.04, modelName,  0.45, 255, 255, 255)
                    DrawLabel(0.5, 0.09, autoLabel,  0.35, autoColR, autoColG, autoColB)

                    if autoMode then
                        local rem = math.max(0.0, AUTO_DELAY - autoElapsed)
                        DrawLabel(0.5, 0.14, ('Capturing in %.1fs...'):format(rem), 0.32, 255, 200, 50)
                    end

                    if gHeld then
                        DrawLabel(0.5, 0.88, ('Grab dist: %.1f  |  Q / E to rotate'):format(grabDist), 0.30, 100, 220, 255)
                        DrawLabel(0.5, 0.92, 'Aim to place    Scroll = push/pull    Release G = drop', 0.26, 160, 200, 255)
                    else
                        DrawLabel(0.5, 0.88, 'E / LMB  Capture    R  Skip    TAB  Auto    BACKSPACE  Quit', 0.28, 220, 220, 220)
                        DrawLabel(0.5, 0.92, 'Hold G:  Aim to place    Scroll = dist    Q/E = rotate',      0.26, 160, 160, 160)
                    end
                    -- ─────────────────────────────────────────────────────────

                    -- ── Input ────────────────────────────────────────────────
                    if IsControlJustPressed(0, 38) or IsDisabledControlJustPressed(0, 24) then
                        action = 'capture'
                    elseif IsDisabledControlJustPressed(0, 45) then
                        action = 'skip'
                    elseif IsControlJustPressed(0, 177) then
                        action = 'quit'
                    elseif IsDisabledControlJustPressed(0, 37) then
                        autoMode    = not autoMode
                        autoElapsed = 0.0
                    end

                    -- Auto-capture countdown
                    if autoMode then
                        autoElapsed = autoElapsed + GetFrameTime()
                        if autoElapsed >= AUTO_DELAY then
                            action = 'capture'
                        end
                    end
                    -- ─────────────────────────────────────────────────────────

                    Wait(0)
                end

                if action == 'capture' then
                    done = false
                    TriggerServerEvent('dnx_thumbgen:capture', modelName, SKIP_EXISTING)

                    local waited = 0
                    while not done and waited < 8000 do
                        HideHudAndRadarThisFrame()
                        Wait(100)
                        waited = waited + 100
                    end
                elseif action == 'skip' then
                    skipped = skipped + 1
                    print(('[DNX Thumbgen] SKIP (manual): %s'):format(modelName))
                elseif action == 'quit' then
                    quit = true
                end

                DeleteObject(obj)
                activeObj = nil
                SetModelAsNoLongerNeeded(hash)
                Wait(100)
            end

            if i % 25 == 0 then
                print(('[DNX Thumbgen] Progress: %d / %d'):format(i, total))
            end
        end

        SetEntityVisible(ped, true, false)
        inSession = false

        if quit then
            print('[DNX Thumbgen] Session quit by user.')
        else
            print(('[DNX Thumbgen] Complete! %d captured, %d skipped.'):format(total - skipped, skipped))
            print('[DNX Thumbgen] Files saved to: resources/[tools]/dnx_thumbgen/output/')
        end
    end)
end, false)
